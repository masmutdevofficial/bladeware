<template>
  <div class="z-0 relative w-full h-screen flex justify-center">
    <!-- Background Image -->
    <img
      src="@/assets/img/my1.webp"
      class="absolute inset-0 w-[100%] lg:w-[50%] right-0 top-0 left-auto object-cover z-0"
      alt="Background"
    />

    <!-- CARD -->
    <div
      class="relative z-10 bg-white h-max mt-[30px] py-5 px-5 lg:py-5 lg:px-5 rounded-lg flex flex-col items-center md:flex-row md:justify-between w-[95%] lg:w-200 bg-opacity-90"
    >
      <div class="w-full p-4">
        <!-- Tabs -->
        <div class="flex justify-around border-b-2 border-gray-200 mb-4">
          <div
            v-for="tab in tabs"
            :key="tab"
            @click="selectedTab = tab"
            :class="[
              'font-semibold pb-2 cursor-pointer',
              selectedTab === tab
                ? 'text-orange-500 border-b-2 border-orange-500'
                : 'text-gray-500',
            ]"
          >
            {{ tab }}
          </div>
        </div>

        <!-- Transaction List -->
        <div v-if="filteredRecords.length">
          <div
            v-for="(record, index) in filteredRecords"
            :key="index"
            class="mb-4"
          >
            <div
              class="flex items-center justify-between text-sm text-gray-500"
            >
              <span>{{ record.created_at }}</span>

              <div class="flex flex-col items-end justify-end">
                <span class="text-gray-500">
                  {{ selectedTab === "Deposit" ? "Deposit" : "" }}
                </span>
                <div
                  class="font-semibold"
                  :class="{
                    'text-yellow-500': record.status == 0,
                    'text-green-600': record.status == 1,
                    'text-red-500': record.status == 2,
                  }"
                >
                  {{
                    record.status == 0
                      ? "On Process"
                      : record.status == 1
                      ? "Completed"
                      : record.status == 2
                      ? "Rejected"
                      : "-"
                  }}
                </div>
              </div>
            </div>

            <div class="flex items-center justify-between">
              <div class="mt-2">
                <div class="text-sm text-start text-gray-500">Network</div>
                <div class="font-semibold">{{ record.network_address }}</div>
              </div>

              <div class="mt-2">
                <div class="text-sm text-end text-gray-500">Currency</div>
                <div class="font-semibold">{{ record.currency }}</div>
              </div>
            </div>

            <div class="grid grid-cols-2 gap-4 items-center mt-2">
              <div>
                <div class="text-sm text-start text-gray-500">Wallet</div>
                <div class="font-semibold break-all">
                  {{ record.wallet_address }}
                </div>
              </div>

              <div class="text-right">
                <div class="text-sm text-gray-500">Amount</div>
                <div class="font-semibold">
                  {{ record.amount }} {{ record.currency }}
                </div>
              </div>
            </div>

            <hr class="text-gray-300 my-4" />
          </div>
        </div>
        <!-- No Data -->
        <div
          v-else
          class="flex flex-col justify-center items-center text-center text-gray-400 py-10"
        >
          <img src="@/assets/img/default.png" style="width: 150px" />
          <p>No Data Available</p>
        </div>
      </div>
    </div>
  </div>

  <!-- Modal -->
  <div
    v-if="alert.message"
    class="fixed inset-0 flex items-center justify-center bg-black/30 z-50"
  >
    <div
      :class="[
        'p-4 rounded-lg flex flex-col justify-center items-center text-center w-[300px]',
        'bg-black/70 text-white',
      ]"
    >
      <!-- SVG Ikon -->
      <div class="mb-3">
        <!-- Success Icon -->
        <svg
          v-if="alert.type === 'success'"
          xmlns="http://www.w3.org/2000/svg"
          width="50"
          height="50"
          viewBox="0 0 24 24"
          fill="currentColor"
        >
          <path d="M0 0h24v24H0z" fill="none" />
          <path
            d="M17 3.34a10 10 0 1 1 -14.995 8.984l-.005 -.324l.005 -.324a10 10 0 0 1 14.995 -8.336zm-1.293 5.953a1 1 0 0 0 -1.32 -.083l-.094 .083l-3.293 3.292l-1.293 -1.292l-.094 -.083a1 1 0 0 0 -1.403 1.403l.083 .094l2 2l.094 .083a1 1 0 0 0 1.226 0l.094 -.083l4 -4l.083 -.094a1 1 0 0 0 -.083 -1.32z"
          />
        </svg>
        <!-- Error Icon -->
        <svg
          v-else
          xmlns="http://www.w3.org/2000/svg"
          width="50"
          height="50"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          stroke-width="2"
          stroke-linecap="round"
          stroke-linejoin="round"
        >
          <path stroke="none" d="M0 0h24v24H0z" fill="none" />
          <path
            d="M12 21a9 9 0 0 0 9 -9a9 9 0 0 0 -9 -9a9 9 0 0 0 -9 9a9 9 0 0 0 9 9z"
          />
          <path d="M9 8l6 8" />
          <path d="M15 8l-6 8" />
        </svg>
      </div>
      <!-- Pesan Alert -->
      <p>{{ alert.message }}</p>
    </div>
  </div>
</template>

<script setup>
import { ref, computed, onMounted } from "vue";
import axios from "axios";

// Tabs
const tabs = ["Deposit", "Withdrawal"];
const selectedTab = ref("Deposit");

// Alert
const alert = ref({ message: "", type: "success" });
const showAlert = (message, type = "error") => {
  alert.value = { message, type };
  setTimeout(() => (alert.value.message = ""), 3000);
};

// Data
const deposits = ref([]);
const withdrawals = ref([]);

const fetchFinance = async () => {
  try {
    const jwtToken = localStorage.getItem("jwt_token");
    if (!jwtToken) throw new Error("No token found");

    const { data } = await axios.get(
      "https://bladeware.masmut.dev/api/get-finance",
      {
        headers: { Authorization: `Bearer ${jwtToken}` },
      }
    );

    if (data.status === "success") {
      deposits.value = data.data.deposits || [];
      withdrawals.value = data.data.withdrawals || [];
    } else if (data.message === "Transaction not found") {
      deposits.value = [];
      withdrawals.value = [];
    } else {
      showAlert(data.message || "Failed to load data", "error");
    }
  } catch (error) {
    const message = error?.response?.data?.message || error.message;
    if (message !== "Transaction not found") {
      showAlert(message, "error");
    }
  }
};

// Computed for selected tab
const filteredRecords = computed(() => {
  return selectedTab.value === "Deposit" ? deposits.value : withdrawals.value;
});

// On load
onMounted(() => {
  fetchFinance();
});
</script>
